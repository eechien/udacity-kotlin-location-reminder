@RunWith(AndroidJUnit4::class)
@ExperimentalCoroutinesApi
//UI Testing
@MediumTest
class ReminderListFragmentTest : AutoCloseKoinTest(){

    private lateinit var repository: ReminderDataSource
    private lateinit var appContext: Application

    class ToastMatcher : TypeSafeMatcher<Root?>() {
        override fun describeTo(description: Description) {
            description.appendText("is toast")
        }
        override fun matchesSafely(root: Root?): Boolean {
            val type: Int? = root?.windowLayoutParams?.get()?.type
            if (type == WindowManager.LayoutParams.TYPE_APPLICATION_OVERLAY) {
                val windowToken: IBinder? = root.decorView?.windowToken
                val appToken: IBinder? = root.decorView?.applicationWindowToken
                if (windowToken === appToken) {
                    // windowToken == appToken means this window isn't contained by any other windows.
                    // if it was a window for an activity, it would have TYPE_BASE_APPLICATION.
                    return true
                }
            }
            return false
        }
    }

    fun isToast(): Matcher<Root?> {
        return ToastMatcher()
    }

    @Before
    fun init() {
        stopKoin()//stop the original app koin
        appContext = getApplicationContext()
        val myModule = module {
            viewModel {
                RemindersListViewModel(
                    appContext,
                    get() as ReminderDataSource
                )
            }
            single {
                SaveReminderViewModel(
                    appContext,
                    get() as ReminderDataSource
                )
            }
            single { RemindersLocalRepository(get()) as ReminderDataSource }
            single { LocalDB.createRemindersDao(appContext) }
        }
        //declare a new koin module
        startKoin {
            modules(listOf(myModule))
        }
        //Get our real repository
        repository = get()

        //clear the data to start fresh
        runBlocking {
            repository.deleteAllReminders()
        }
    }

    @Test
    fun addReminder_navigateToSaveReminder() {
        val scenario = launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)
        val navController = Mockito.mock(NavController::class.java)
        scenario.onFragment {
            Navigation.setViewNavController(it.view!!, navController)
        }
        onView(withId(R.id.addReminderFAB)).perform(ViewActions.click())
        Mockito.verify(navController).navigate(ReminderListFragmentDirections.toSaveReminder())
    }

    @Test
    fun noDataDisplayed() {
        launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)
        onView(withText(R.string.no_data)).check(matches(isDisplayed()))
    }

    @Test
    fun remindersDisplayed() {
        val reminder1 = ReminderDTO("title1", "description1", "location1", 1.01, 1.02)
        val reminder2 = ReminderDTO("title2", "description2", "location2", 2.01, 2.02)
        runBlocking {
            repository.saveReminder(reminder1)
            repository.saveReminder(reminder2)
        }
        launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)
        onView(withText(reminder1.title)).check(matches(isDisplayed()))
        onView(withText(reminder1.description)).check(matches(isDisplayed()))
        onView(withText(reminder1.location)).check(matches(isDisplayed()))
    }
}